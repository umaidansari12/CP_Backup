// Function to reverse first k elements of a queue.
queue<int> modifyQueue(queue<int> q, int k) {
    // add code here.
    vector<int> r;
    while (!q.empty()) {
        r.push_back(q.front());
        q.pop();
    }
    reverse(r.begin(), r.begin() + k);
    for (auto i : r)
        q.push(i);
    return q;

}