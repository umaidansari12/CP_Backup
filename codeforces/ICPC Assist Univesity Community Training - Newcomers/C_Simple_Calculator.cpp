#include <bits/stdc++.h>

using namespace std;
using ll = long long;

int main()
{
	ios_base :: sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
	long long int x, y;
	cin >> x >> y;

	cout << x << " + " << y << " = " << x + y << "\n";
	cout << x << " * " << y << " = " << x * y << "\n";
	cout << x << " - " << y << " = " << x - y << "\n";


	return 0;
}